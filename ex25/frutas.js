var fruta = 'laranja'

switch (fruta) {
    case 'laranja':
        console.log('Essa fruta tem a cor laranja.')
        break
    case 'maçã':
        console.log('Essa fruta tem a cor vermelha.')
    case 'limão':
        console.log('Essa fruta tem a cor verde.')
        break
    case 'tamarindo':
        console.log('Essa fruta tem a cor marrom.')
        break
    case 'banana':
        console.log('Essa fruta tem a cor amarela.')
        break
    default:
        console.log('Essa fruta tem a cor desconhecida.')

}